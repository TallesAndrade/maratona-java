public class Aula05EstruturasCondicionais06 {
    public static void main(String[] args) {
        byte dia = 5;
        switch (dia){
            default:
                System.out.println("Invalid Option");
            case 1:
                System.out.println("Weekend");
                break;
            case 2:
                System.out.println("Business day");
                break;
            case 3:
                System.out.println("Business day");
                break;
            case 4:
                System.out.println("Business day ");
                break;
            case 5:
                System.out.println("Business day ");
                break;
            case 6:
                System.out.println("Business day ");
                break;
            case 7:
                System.out.println("Weekend ");
                break;
        }
    }
}
